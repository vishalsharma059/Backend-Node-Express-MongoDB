// What is JavaScript?

// JavaScript is a high-level, lightweight, and interpreted programming language used to make web pages interactive and dynamic. 
// It is one of the core web technologies alongside HTML and CSS. JavaScript can run on both the client side (in browsers) and the server side via Node.js.


// What is Node.js?

// Node.js is a runtime environment that allows JavaScript to run outside the browser, i.e., on the server side. 
// It is built on Google Chrome's V8 JavaScript engine and follows an event-driven, non-blocking (asynchronous), single-threaded architecture, making it highly scalable and efficient for I/O-intensive applications.
